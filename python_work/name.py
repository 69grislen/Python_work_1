name = "ada lovelace"

print(name.title()) #Метод title() преобазует первый символ каждого слова к верхнему регистру (заглавной букве)

name = "Adas Lovelace"

print(name.upper()) #Метод upper() преобразует все буквы к верхнему регистру (т.е все буквы заглавные)
print(name.lower()) #Метод lower() преобразует все буквы к нижнему регистру (т.е. делает х маленькими)


